//Entree variables
var entreesButton = document.querySelector("#entreesButton");
var entreesMenu = document.querySelector(".entreesMenu");
var exitEntrees = document.querySelector("#exitEntrees");

//Wine actions
entreesButton.addEventListener("click", function() {

  entreesMenu.style.opacity=1;
  // wineMenu.style.display="block";
  entreesMenu.style.visibility="visible";

});

exitEntrees.addEventListener("click", function() {

  entreesMenu.style.opacity=0;
	// wineMenu.style.display="none";
  entreesMenu.style.visibility="hidden";

});




